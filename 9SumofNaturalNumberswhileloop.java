class Sumofnaturalnumberswhile
{
    public static void main(String args[])
    {
        int n=20;
        int sum=0;
        int i=1;
        while(i<=n)
        
        {
           sum=sum+i; 
           i++;
        }
        System.out.println("Sum of " + n + " numbers is " +sum);
    }   

}