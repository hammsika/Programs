class HalfPyramidAlphabets
{
    public static void main(String args[])
    {
        char gg='E';
        for(char i='A';i<=gg;i++)
        {
            for (char j='A';j<=i;j++)
            {
                System.out.print(i +" ");

            }
            System.out.println("");
        }
    }

}