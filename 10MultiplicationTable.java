class multiplicationtable
{
    public static void main(String args[])
    {
        int n=5;
        int m=12;
        for(int i=1;i<=n;i++)
        {
            for(int j=1;j<=m;j++)
            {
                System.out.println(i + "*" +j +"="+(j*i));
            }
           
        }

    }

}